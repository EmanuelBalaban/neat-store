class Constants {
  const Constants._();

  /// [superscriptDigits] is a list of superscript unicode characters that can
  /// be used to display superscript digits.
  /// Use case: superscriptDigits[digit] returns the superscript character of
  /// the given digit.
  static const superscriptDigits = [
    '\u2070',
    '\u00B9',
    '\u00B2',
    '\u00B3',
    '\u2074',
    '\u2075',
    '\u2076',
    '\u2077',
    '\u2078',
    '\u2079',
  ];
}
